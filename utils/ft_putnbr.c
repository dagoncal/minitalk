#include "minitalk.h"

void	ft_putnbr(int n)
{
	char	nbr;

	if (n >= 10)
	{
		ft_putnbr(n / 10);
		nbr = n % 10 + 48;
		write(1, &nbr, 1);
	}
	else if (n >= 0 && n < 10)
	{
		nbr = n + 48;
		write(1, &nbr, 1);
	}
	else if (n == -2147483648)
	{
		ft_putstr("-2147483648");
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-n);
	}
}
